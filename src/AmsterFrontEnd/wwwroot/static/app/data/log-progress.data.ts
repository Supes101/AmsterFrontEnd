export class LogProgress {
    bytes_parsed: string;
    bytes_uploaded: string;
    message: string;
    parse_percent: string;
    total_bytes: string;
    upload_percent: string;

    LogProgress() {
        this.bytes_parsed = "0";
        this.bytes_parsed = "0";
        this.message = "";
        this.parse_percent = "0";
        this.total_bytes = "0";
        this.upload_percent = "0";
    }

}